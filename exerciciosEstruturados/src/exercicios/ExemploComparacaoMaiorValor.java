package exercicios;

public class ExemploComparacaoMaiorValor {

	public static void main(String[] args) {
		int a = 10;
		int b = 5;
		
		if(a > b) {
			System.out.println("A é maior que B");
		}else if (a == b){
			System.out.println("A é igual a B");
		}else {
			System.out.println("B é maior que A");		
		}

	}

}
